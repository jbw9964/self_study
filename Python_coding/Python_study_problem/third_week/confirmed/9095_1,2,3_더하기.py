# 답은 구했지만 계산과정(?) 은 이해 못함.

Num = int(input())

D = [0 for i in range(12)]
D[1] = 1
D[2] = 2
D[3] = 4
for i in range(4, 12) : 
    D[i] = D[i - 1] + D[i - 2] + D[i - 3]

temp = []
for i in range(Num) : 
    temp.append(int(input()))

for value in temp : 
    print(D[value])




